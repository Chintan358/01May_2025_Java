package operators;

public class Arithmatic {
    public static void main(String[] args) {

        //+, -, *, /, %

        double a = 10;
        double b = 20;

        System.out.println(a+b);
        System.out.println(a+"hello");
        System.out.println(a+b+"Hello");
        System.out.println("Hello"+a+b);

        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);


    }
}
